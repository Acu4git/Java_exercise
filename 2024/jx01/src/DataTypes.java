public class DataTypes {

  public static void main(String[] args) {
    // 整数型
    byte b = 1;
    short s = 2;
    int i = 3;
    long l = 4;

    System.out.println("byte: " + b); // 1
    System.out.println("short: " + s); // 2
    System.out.println("int: " + i); // 3
    System.out.println("long: " + l); // 4

    // 浮動小数点型
    float f = 1.0f;
    double d = 2.0;

    System.out.println("float: " + f); // 1.0
    System.out.println("double: " + d); // 2.0

    // 文字型
    char c = 'a';
    String str = "Hello World!";

    System.out.println("char: " + c); // a
    System.out.println("string: " + str); // Hello World!

    // 論理型
    boolean b1 = true;
    boolean b2 = false;

    System.out.println("boolean: " + b1); // true
    System.out.println("boolean: " + b2); // false
  }
}
